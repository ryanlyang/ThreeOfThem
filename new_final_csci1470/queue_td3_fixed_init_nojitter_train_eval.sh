#!/usr/bin/env bash
set -euo pipefail

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
cd "$ROOT_DIR"
mkdir -p logs

PIPE_TAG="${PIPE_TAG:-td3_fixedinit_nojitter_$(date +%Y%m%d_%H%M%S)}"

echo "Submitting TD3 FIXED-INIT NO-JITTER train job with PIPE_TAG=$PIPE_TAG"
TRAIN_JOB_ID=$(
  sbatch --parsable \
    --partition=tier3 \
    --time=24:00:00 \
    --cpus-per-task=8 \
    --mem=32G \
    --export=ALL,PIPE_TAG="$PIPE_TAG",RUN_NAME="td3_fixedinit_nojitter_${PIPE_TAG}",TOTAL_ENV_STEPS=1200000,NUM_ENVS=8,VEC_ENV=subproc,MP_START_METHOD=spawn,FIXED_INIT_PROFILE=offset_ref,FIXED_INIT_POS_JITTER_STD=0.0,FIXED_INIT_VEL_JITTER_STD=0.0,FIXED_INIT_JITTER_TRIES=1,EVAL_FIXED_INIT_POS_JITTER_STD=0.0,EVAL_FIXED_INIT_VEL_JITTER_STD=0.0,EVAL_FIXED_INIT_JITTER_TRIES=1,HORIZON_STEPS=500,MAX_ACTION_NORM=1.8,ESCAPE_RADIUS=8.0,REWARD_SCALE=800.0,W_POS=20.0,W_VEL=12.0,W_FUEL=0.0005,W_NEAR_COLLISION=20.0,W_COLLISION=500.0,W_ESCAPE=50.0,W_SWITCH=0.08,W_PHASE=0.20,BUFFER_SIZE=1000000,BATCH_SIZE=512,LEARNING_STARTS=20000,UPDATES_PER_ITER=2,EXPLORATION_NOISE=0.30,EXPLORATION_NOISE_FINAL=0.05,EXPLORATION_DECAY_STEPS=500000,EVAL_EVERY_ENV_STEPS=25000,EVAL_EPISODES=10,EVAL_POS_THRESHOLD=0.06,EVAL_VEL_THRESHOLD=0.09,EVAL_CONSECUTIVE_CONVERGED=260,EVAL_MIN_TOTAL_STEPS=320,SAVE_TOPK=3,EARLY_STOP_ON_STRICT_SUCCESS=1,EARLY_STOP_SUCCESS_RATE=1.0,EARLY_STOP_MAX_FAILURE_RATE=0.0,EARLY_STOP_PATIENCE_EVALS=1,EARLY_STOP_MIN_EVALS=1 \
    sbatch_train_td3_fixed_init.sh
)

echo "Submitting dependent TD3 FIXED-INIT NO-JITTER eval job afterok:$TRAIN_JOB_ID"
EVAL_JOB_ID=$(
  sbatch --parsable \
    --partition=tier3 \
    --time=01:30:00 \
    --cpus-per-task=4 \
    --mem=24G \
    --dependency=afterok:"$TRAIN_JOB_ID" \
    --export=ALL,PIPE_TAG="$PIPE_TAG",EVAL_EPISODES=12,NUM_SETUPS=1,MAX_STEPS=500,POS_THRESHOLD=0.06,VEL_THRESHOLD=0.09,CONSECUTIVE_CONVERGED=260,MIN_TOTAL_STEPS_FOR_CONVERGED=320,TRAIL_LEN=120,FRAME_STRIDE=1,AXIS_PAD=0.25,FIXED_INIT_POS_JITTER_STD_EVAL=0.0,FIXED_INIT_VEL_JITTER_STD_EVAL=0.0,FIXED_INIT_JITTER_TRIES_EVAL=1 \
    sbatch_eval_td3_fixed_init_after_train.sh
)

echo "TRAIN_JOB_ID=$TRAIN_JOB_ID"
echo "EVAL_JOB_ID=$EVAL_JOB_ID"
echo "PIPE_TAG=$PIPE_TAG"

echo "Track jobs:"
echo "  squeue -u \"$USER\""
echo "Logs:"
echo "  logs/csci1470_td3_train_${TRAIN_JOB_ID}.out"
echo "  logs/csci1470_td3_train_${TRAIN_JOB_ID}.err"
echo "  logs/csci1470_td3_eval_${EVAL_JOB_ID}.out"
echo "  logs/csci1470_td3_eval_${EVAL_JOB_ID}.err"
echo "Pipeline metadata dir: artifacts/pipelines/$PIPE_TAG"
